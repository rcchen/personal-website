		<p>For our CS210 project, our team was tasked with developing a system that would best demonstrate the capabilities of the <a href="http://compuverde.com/">Compuverde</a> distributed large data store to potential customers, with a large focus on <a href="http://en.wikipedia.org/wiki/Call_detail_record">call detail records</a> from telecom companies.</p>

		<p>Our solution focused on allowing nontechnical users create interactive data visualizations using a simple Google Docs-like interface.</p>

		<p>We also implemented and exposed powerful filtering options to the end user in a simple and refined interface.</p>

		<p>The system was built out with Ruby on Rails as our main framework and leveraged MongoDB as a NoSQL datastore that did a good job of simulating Compuverde's key-value store. On the front end, we used Backbone.js and jQuery as our main drivers for providing an interactive experience for our users.</p>

		<p>The biggest thing that I learned from this project was the <strong>importance of transfer of information</strong>. We ran into a situation early in the project where I ended up writing about 90% of the original code base because I was the only one who was familiar with the entire web stack. Even after the initial set up period ended, I still ended up coding many of the features for the project, which led to severe burnout during the second half of the class. Fortunately, one of my team members (Truman) was able to take the lead on backend development, and ended up implementing the queueing system that we used to handle background uploads as well as optimizing the data store.</p>

